class Solution:
    def twoSum(self, numbers: List[int], target: int) -> List[int]:
        # Два указателя: 
        # - left_i начинается с начала массива (самый маленький элемент)
        # - right_i начинается с конца массива (самый большой элемент)
        # Массив numbers гарантированно отсортирован по возрастанию (условие задачи LeetCode #167).
        left_i = 0
        right_i = len(numbers) - 1

        # Основной цикл: движение указателей навстречу друг другу
        # Цикл завершится, как только найдём искомую пару (гарантировано по условию задачи).
        while True:
            current_sum = numbers[left_i] + numbers[right_i]
            
            # Если сумма равна целевому значению — задача решена.
            # Возвращаем индексы в индексации с единица (требование задачи).
            if current_sum == target:
                return [left_i + 1, right_i + 1]  # Оборачиваем в список для соответствия типу возврата
            
            # Если сумма БОЛЬШЕ target:
            # — значит, нужно уменьшить сумму.
            # — так как массив отсортирован, уменьшить сумму можно,
            #   только сдвинув ПРАВЫЙ указатель влево (берём меньшее значение).
            elif current_sum > target:
                right_i -= 1
            
            # Если сумма МЕНЬШЕ target:
            # — нужно увеличить сумму.
            # — сдвигаем ЛЕВЫЙ указатель вправо (берём большее значение).
            else:
                left_i += 1

        
        